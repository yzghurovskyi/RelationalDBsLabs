from configparser import ConfigParser
import os


def config(filename='{0}\database.ini'.format(os.path.dirname(__file__)), section='postgresql'):
    # create a parser
    parser = ConfigParser()
    # read config file
    parser.read(filename)

    # get section, default to postgresql
    db = {}
    if parser.has_section(section):
        params = parser.items(section)
        for param in params:
            db[param[0]] = param[1]
    else:
        raise Exception('Section {0} not found in the {1} file'.format(section, filename))

    return db


def get_url_connection(filename='{0}\database.ini'.format(os.path.dirname(__file__)), section='postgresql'):
    parser = ConfigParser()
    parser.read(filename)
    db = {}
    if parser.has_section(section):
        params = parser.items(section)
        for param in params:
            db[param[0]] = param[1]
    else:
        raise Exception('Section {0} not found in the {1} file'.format(section, filename))
    return "%s://%s:%s@%s/%s" % (section, db['user'], db['password'], db['host'], db['database'])
